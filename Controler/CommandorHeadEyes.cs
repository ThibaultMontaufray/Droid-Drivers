        #region Methods private
        private static void PushTable()
        {
            for (int i = 0; i < 8; i++)
            {
                for (int j = 0; j < 24; j++)
			    {
                    if (j >= 0 && j < 8)
                    {
                        _panelA.Values[i][j] = _eyesMatrice[i][j];
                    }
                    else if (j >= 8 && j < 16)
                    {
                        _panelB.Values[i][j-8] = _eyesMatrice[i][j];
                    }
                    else
                    {
                        _panelC.Values[i][j-16] = _eyesMatrice[i][j];
                    }
			    }
            }
        }
        private static void Refresh()
        {
            _panelA.Process();
            _panelB.Process();
            _panelC.Process();
        }
        private static void CalculNextScreen()
        {
            // next we could program an transition between two screens...
            _actionScreenTransition = _actionScreenTarget;

            switch (_actionScreenTarget)
            {
                case ActionScreen.NORMAL: ScreenNormal(); break;
                case ActionScreen.TOP: ScreenTop(); break;
                case ActionScreen.BOTTOM: ScreenBottom(); break;
                case ActionScreen.RIGHT: ScreenRight(); break;
                case ActionScreen.LEFT: ScreenLeft(); break;
                case ActionScreen.HEART: ScreenHeart(); break;
                case ActionScreen.RANDOM: ScreenRandom(); break;
                case ActionScreen.SURPRISED: ScreenSurprised(); break;
                case ActionScreen.SEPTICAL: ScreenSeptical(); break;
                case ActionScreen.CONTRARIED: ScreenContraried(); break;
                case ActionScreen.SHUTDOWN1: ScreenShutdown1(); break;
                case ActionScreen.SHUTDOWN2: ScreenShutdown2(); break;
                default: ScreenEmpty(); break;
            }
        }

