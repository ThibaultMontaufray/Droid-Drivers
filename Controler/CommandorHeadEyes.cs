        public static void Init()
        {
            _panelA = new LEDMatrice(0);
            _panelB = new LEDMatrice(1);
            _panelC = new LEDMatrice(2);

            int[] row;
            _eyesMatrice = new int[8][];
            for (int i = 0; i < _eyesMatrice.Length; i++)
            {
                row = new int[24];
                for (int j = 0; j < 24; j++)
                {
                    row[j] = 0;
                }
                _eyesMatrice[i] = row;
            }
        }
        public static void Process()
        {
            CalculNextScreen();
            PushTable();
            Refresh();
        }
